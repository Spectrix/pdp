#include <iostream>
using namespace std;

int main() {
	int bil1 = 70;
	int bil2 = 49;

	cout << "Penjumlahan: " << (bil1 + bil2) << endl;
	cout << "Pengurangan: " << (bil1 - bil2) << endl;
	cout << "Perkalian: " << (bil1 * bil2) << endl;
	cout << "Pembagian: " << (bil1 / bil2) << endl;
	cout << "Modulus: " << (bil1 % bil2) << endl;

	return 0;
}
